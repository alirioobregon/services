        super.onCreate(savedInstanceState)
        setContent {
            ServicesProjectTheme {
                // A surface container using the 'background' color from the theme
                Surface(modifier = Modifier.fillMaxSize(), color = MaterialTheme.colorScheme.background) {
                    AppNavigation()
                }
            }
        }
    }
}

@Composable
fun AppNavigation() {
    val navController = rememberNavController()
    NavHost(navController = navController, startDestination = "login") {
        composable("login") {
            LoginScreen(navController)
        }
        composable("main") {
            MainScreen(
                onNavigationSuccess = { service ->
                    navController.navigate("detail/$service")
                })

        }
        composable(
            "detail/{service}",
            arguments = listOf(navArgument("service") { type = NavType.StringType })
        ) { backStackEntry ->
            val service = backStackEntry.arguments?.getString("service").toString()
            DetailScreen(service = service)
        }

        composable("register"){
            RegisterScreen(navHostController = navController)
        }
    }
}

